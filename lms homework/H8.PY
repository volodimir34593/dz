import random

class MoodBot:
    def __init__(self):
        self.mood = 'neutral'
        self.responses = {
            'greeting': ['–ü—Ä–∏–≤—ñ—Ç!', '–ü—Ä–∏–≤—ñ—Ç-–ø—Ä–∏–≤—ñ—Ç!', '–í—ñ—Ç–∞—é –≤–∞—Å!'],
            'farewell': ['–î–æ –ø–æ–±–∞—á–µ–Ω–Ω—è!', '–ë—É–≤–∞–π—Ç–µ!', '–î–æ –∑—É—Å—Ç—Ä—ñ—á—ñ!'],
            'neutral': ['–¶—ñ–∫–∞–≤–æ...', '–î–æ–±—Ä–µ.', '–ù–µ –∑–æ–≤—Å—ñ–º –∑—Ä–æ–∑—É–º—ñ–ª–æ.'],
            'happy': ['–í–∏–±—ñ–ª—å—à–µ–Ω–∏–π –Ω–∞—Å—Ç—Ä—ñ–π! üòÑ', '–°—É–ø–µ—Ä, —â–æ –≤–∏ —Ç–∞–∫ –≤–µ—Å–µ–ª—ñ!', '–†–∞–¥—ñ—é –∑–∞ –≤–∞—Å!'],
            'sad': ['–°—É–º–Ω–æ —á—É—Ç–∏...', '–Ø–∫ –º–æ–∂–Ω–∞ –¥–æ–ø–æ–º–æ–≥—Ç–∏?', '–°–ø–æ–¥—ñ–≤–∞—é—Å—å, —â–æ –≤–∏ —Å–∫–æ—Ä–æ –ø–æ—á—É—î—Ç–µ—Å—è –∫—Ä–∞—â–µ.']
        }

    def set_mood(self, mood):
        self.mood = mood

    def get_response(self, user_input):
        if '–ø—Ä–∏–≤—ñ—Ç' in user_input.lower():
            return random.choice(self.responses['greeting'])
        elif '–¥–æ –ø–æ–±–∞—á–µ–Ω–Ω—è' in user_input.lower():
            return random.choice(self.responses['farewell'])
        elif '–Ω–∞—Å—Ç—Ä—ñ–π' in user_input.lower():
            return f"–í–∞—à –ø–æ—Ç–æ—á–Ω–∏–π –Ω–∞—Å—Ç—Ä—ñ–π: {self.mood.capitalize()}."
        elif '–≤—Å—Ç–∞–Ω–æ–≤–∏—Ç–∏ –Ω–∞—Å—Ç—Ä—ñ–π' in user_input.lower():
            parts = user_input.lower().split()
            if len(parts) >= 3:
                new_mood = parts[2]
                if new_mood in ['neutral', 'happy', 'sad']:
                    self.set_mood(new_mood)
                    return f"–ù–∞—Å—Ç—Ä—ñ–π –≤—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ –Ω–∞ {new_mood.capitalize()}."
                else:
                    return "–ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–∏–π –Ω–∞—Å—Ç—Ä—ñ–π. –î–æ—Å—Ç—É–ø–Ω—ñ –≤–∞—Ä—ñ–∞–Ω—Ç–∏: neutral, happy, sad."
            else:
                return "–ë—É–¥—å –ª–∞—Å–∫–∞, –≤–≤–µ–¥—ñ—Ç—å –Ω–∞—Å—Ç—Ä—ñ–π, –Ω–∞–ø—Ä–∏–∫–ª–∞–¥: '–≤—Å—Ç–∞–Ω–æ–≤–∏—Ç–∏ –Ω–∞—Å—Ç—Ä—ñ–π —Ä–∞–¥—ñ—Å–Ω–∏–π'."
        else:
            return random.choice(self.responses[self.mood])


# –ü—Ä–∏–∫–ª–∞–¥ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è:

mood_bot = MoodBot()

while True:
    user_input = input("–í–≤–µ–¥—ñ—Ç—å –≤–∞—à–µ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è: ")
    if user_input.lower() == '–≤–∏–π—Ç–∏':
        print("–î—è–∫—É—é –∑–∞ —Ä–æ–∑–º–æ–≤—É! –î–æ –ø–æ–±–∞—á–µ–Ω–Ω—è!")
        break
    else:
        response = mood_bot.get_response(user_input)
        print("–ë–æ—Ç:", response)
